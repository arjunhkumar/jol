package in.ac.iitmandi.compl.ds;

/**
 * @author arjun
 *
 */
public class CustomIntDS {

	private int customerX;
	private int customerY;
	private int customerZ;
	
	public CustomIntDS(int customerX, int customerY, int customerZ) {
		this.customerX = customerX;
		this.customerY = customerY;
		this.customerZ = customerZ;
	}
	
	/**
	 * @return the customerX
	 */
	public int getCustomerX() {
		return customerX;
	}
	/**
	 * @param customerX the customerX to set
	 */
	public void setCustomerX(int customerX) {
		this.customerX = customerX;
	}
	/**
	 * @return the customerY
	 */
	public int getCustomerY() {
		return customerY;
	}
	/**
	 * @param customerY the customerY to set
	 */
	public void setCustomerY(int customerY) {
		this.customerY = customerY;
	}
	/**
	 * @return the customerZ
	 */
	public int getCustomerZ() {
		return customerZ;
	}
	/**
	 * @param customerZ the customerZ to set
	 */
	public void setCustomerZ(int customerZ) {
		this.customerZ = customerZ;
	}

	
	
}
